    private void btnBrowseBackgroundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseBackgroundActionPerformed
        
        try
        {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setFileFilter(new FileNameExtensionFilter("Portable Net Graphics (*.png)", "png"));
            int result = fileChooser.showOpenDialog(this);
            if (result == JFileChooser.APPROVE_OPTION)
            {
                File f = fileChooser.getSelectedFile();
                if (!f.exists())
                    throw new Exception("File doesn't exists!");

                txtBackground.setText(f.getAbsolutePath());
                
            }
        } catch (Exception e)
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.toString(), "Cannot find background image", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBrowseBackgroundActionPerformed

