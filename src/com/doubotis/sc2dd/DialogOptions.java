    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        
        // Commit options changes.
        Preferences.Editor ed = App.getApp().getPreferences().editor();
        
        if (rdbColor.isSelected())
            ed.putString("options.editor.background", "color:" + ColorUtils.fromColor(pnlColor.getBackground()));
        else if (rdbBackground.isSelected())
            ed.putString("options.editor.background", "bg:" + txtBackground.getText());
        
        ed.putBoolean("options.editor.grid", chkDisplayGrid.isSelected());
        ed.putBoolean("options.editor.snap", chkEnableSnap.isSelected());
        ed.putInt("options.editor.grid.width", Integer.parseInt(txtGridWidth.getText()));
        ed.putInt("options.editor.grid.height", Integer.parseInt(txtGridHeight.getText()));
        ed.commit();
                
        dispose();
    }//GEN-LAST:event_btnOKActionPerformed

